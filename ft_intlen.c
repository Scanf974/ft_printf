#include "ft_printf.h"

int		ft_intlen(int n)
{
	size_t		len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}
